/*

This is K1n9_Duk3's hacked up version of the Graphics .H file that IGRAB used
to create for the original games. Since the original file was generated
automatically when grabbing all the images and creating the ?GAGRAPH file, it
was okay to use absolute numbers for the enum and the *START defines.

But since we're not using IGRAB anymore, it doesn't make sense to use any
absolute values in here, as that would mean you would have to change pretty
much every single number if you want to add or remove one of the graphics
chunks.

K1n9_Duk3 didn't want to go through all that trouble in the future and decided
to remove as many absolute values as possible and let the compiler figure out
the rest.

Just keep in mind that all the elements of the enum have to be in the same
order as they are in the ?GAGRAPH file. Remove the elements from the enum when
you remove something from the ?GAGRAPH file and add new elements (at the right
position) when you add new graphics or "externs".

*/

